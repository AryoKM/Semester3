// 1. **Tugas A** harus diselesaikan sebelum **Tugas B**.
// 2. **Tugas C** harus selesai setelah **Tugas A** dan **Tugas B**, tetapi sebelum **Tugas D**.
// 3. **Tugas E** bisa diselesaikan kapan saja (tidak memiliki ketergantungan dengan tugas lain).

// ### Ketergantungan Tugas:
// - **Tugas A** → **Tugas B** → **Tugas C** → **Tugas D**
// - **Tugas E** (bebas, bisa dilakukan kapan saja)

// ### Urutan yang tepat:
// 1. **Tugas A** harus diselesaikan terlebih dahulu karena menjadi dasar bagi tugas lain.
// 2. Setelah **Tugas A**, kerjakan **Tugas B** karena harus diselesaikan sebelum **Tugas C**.
// 3. Setelah **Tugas B**, kerjakan **Tugas C** karena harus selesai sebelum **Tugas D**.
// 4. Setelah **Tugas C**, kerjakan **Tugas D**.
// 5. **Tugas E** bisa diselesaikan kapan saja, jadi bisa dikerjakan di sela-sela atau di akhir, tergantung situasi.

// ### Urutan yang efisien untuk menyelesaikan tugas:
// 1. Tugas A
// 2. Tugas B
// 3. Tugas C
// 4. Tugas D
// 5. Tugas E

